    initializeGame() {
        (this.secretName = this.getRandomName()),
            (this.currentRow = 0),
            (this.currentCol = 0),
            (this.gameOver = !1),
            (this.guesses = []),
            (this.keyStates = {}),
            this.createGrid(),
            this.createKeyboard(),
            this.updateAttemptsDisplay(),
            this.clearMessage(),
            console.log("Secret name:", this.secretName);
    }
    getRandomName() {
        const e = new Date(),
            E =
                Math.floor((e - new Date(e.getFullYear(), 0, 0)) / 864e5) %
                this.targetNames.length;
        return this.targetNames[E];
    }
    createGrid() {
        const e = document.getElementById("game-grid");
        e.innerHTML = "";
        for (let E = 0; E < 6; E++) {
            const S = document.createElement("div");
            S.className = "grid-row";
            for (let e = 0; e < 5; e++) {
                const t = document.createElement("div");
                (t.className = "tile"), (t.id = `tile-${E}-${e}`), S.appendChild(t);
            }
            e.appendChild(S);
        }
    }
    createKeyboard() {
        const e = document.getElementById("keyboard");
        e.innerHTML = "";
        [
            ["Q", "W", "E", "R", "T", "Y", "U", "I", "O", "P"],
            ["A", "S", "D", "F", "G", "H", "J", "K", "L"],
            ["ENTER", "Z", "X", "C", "V", "B", "N", "M", "BACKSPACE"]
        ].forEach((E) => {
            const S = document.createElement("div");
            (S.className = "keyboard-row"),
                E.forEach((e) => {
                    const E = document.createElement("button");
                    (E.className = "key"),
                        (E.textContent = "BACKSPACE" === e ? "" : e),
                        E.setAttribute("data-key", e),
                        "ENTER" === e || "BACKSPACE" === e
                            ? E.classList.add("wide")
                            : E.classList.add("letter"),
                        E.addEventListener("click", () => this.handleKeyPress(e)),
                        S.appendChild(E);
                }),
                e.appendChild(S);
        });
    }
    setupEventListeners() {
        document.addEventListener("keydown", (e) => {
            const E = e.key.toUpperCase();
            "ENTER" === E
                ? this.handleKeyPress("ENTER")
                : "BACKSPACE" === E
                    ? this.handleKeyPress("BACKSPACE")
                    : E.match(/[A-Z]/) && 1 === E.length && this.handleKeyPress(E);
        }),
            document.getElementById("help-btn").addEventListener("click", () => {
                document.getElementById("help-modal").style.display = "block";
            }),
            document.getElementById("stats-btn").addEventListener("click", () => {
                this.updateStatsDisplay(),
                    (document.getElementById("stats-modal").style.display = "block");
            }),
            document.getElementById("new-game-btn").addEventListener("click", () => {
                this.initializeGame();
            }),
            document.querySelectorAll(".close").forEach((e) => {
                e.addEventListener("click", (e) => {
                    e.target.closest(".modal").style.display = "none";
                });
            }),
            window.addEventListener("click", (e) => {
                e.target.classList.contains("modal") && (e.target.style.display = "none");
            });
    }
    handleKeyPress(e) {
        this.gameOver ||
            ("ENTER" === e
                ? this.submitGuess()
                : "BACKSPACE" === e
                    ? this.deleteLetter()
                    : e.match(/[A-Z]/) && this.currentCol < 5 && this.addLetter(e));
    }
    addLetter(e) {
        if (this.currentCol < 5) {
            const E = document.getElementById(
                `tile-${this.currentRow}-${this.currentCol}`
            );
            (E.textContent = e), E.classList.add("filled"), this.currentCol++;
        }
    }
    deleteLetter() {
        if (this.currentCol > 0) {
            this.currentCol--;
            const e = document.getElementById(
                `tile-${this.currentRow}-${this.currentCol}`
            );
            (e.textContent = ""), e.classList.remove("filled");
        }
    }
    submitGuess() {
        if (5 !== this.currentCol)
            return void this.showMessage("Not enough letters", "error");
        const e = this.getCurrentGuess();
        if (!this.isValidGuess(e))
            return this.showMessage("Not in word list", "error"), void this.shakeRow();
        this.guesses.push(e),
            this.evaluateGuess(e),
            this.updateKeyboard(e),
            e === this.secretName
                ? ((this.gameOver = !0),
                    this.showMessage("Congratulations! ", "success"),
                    this.updateStats(!0),
                    setTimeout(() => this.showNameInfo(), 2e3))
                : 5 === this.currentRow
                    ? ((this.gameOver = !0),
                        this.showMessage(`The name was: ${this.secretName}`, "info"),
                        this.updateStats(!1),
                        setTimeout(() => this.showNameInfo(), 2e3))
                    : (this.currentRow++, (this.currentCol = 0), this.updateAttemptsDisplay());
    }
    getCurrentGuess() {
        let e = "";
        for (let E = 0; E < 5; E++) {
            e += document.getElementById(`tile-${this.currentRow}-${E}`).textContent;
        }
        return e;
    }
    isValidGuess(e) {
        return this.validGuesses.includes(e);
    }
    evaluateGuess(e) {
        const E = [],
            S = this.secretName.split(""),
            t = e.split("");
        for (let e = 0; e < 5; e++)
            t[e] === S[e] && ((E[e] = "correct"), (S[e] = null), (t[e] = null));
        for (let e = 0; e < 5; e++)
            if (null !== t[e]) {
                const A = S.indexOf(t[e]);
                -1 !== A ? ((E[e] = "present"), (S[A] = null)) : (E[e] = "absent");
            }
        for (let e = 0; e < 5; e++) {
            const S = document.getElementById(`tile-${this.currentRow}-${e}`);
            setTimeout(() => {
                S.classList.add(E[e]);
            }, 100 * e);
        }
    }
    updateKeyboard(e) {
        const E = this.getGuessResult(e);
        for (let S = 0; S < 5; S++) {
            const t = e[S],
                A = E[S];
            this.keyStates[t] && "absent" !== this.keyStates[t]
                ? "present" === this.keyStates[t] &&
                "correct" === A &&
                (this.keyStates[t] = "correct")
                : (this.keyStates[t] = A);
            const R = document.querySelector(`[data-key="${t}"]`);
            R && (R.className = `key letter ${this.keyStates[t]}`);
        }
    }
    getGuessResult(e) {
        const E = [],
            S = this.secretName.split(""),
            t = e.split("");
        for (let e = 0; e < 5; e++)
            t[e] === S[e] && ((E[e] = "correct"), (S[e] = null), (t[e] = null));
        for (let e = 0; e < 5; e++)
            if (null !== t[e]) {
                const A = S.indexOf(t[e]);
                -1 !== A ? ((E[e] = "present"), (S[A] = null)) : (E[e] = "absent");
            }
        return E;
    }
    shakeRow() {
        document.querySelectorAll(`[id^="tile-${this.currentRow}-"]`).forEach((e) => {
            (e.style.animation = "shake 0.5s"),
                setTimeout(() => {
                    e.style.animation = "";
                }, 500);
        });
    }
